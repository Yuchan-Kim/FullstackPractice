package com.javaex.practice01;

public class Ex13 {
	public static void main (String[] agrs) {
		System.out.println(13/5); //정수 나눗
		System.out.println((double)13/5); //double 형변환
		System.out.println(13/(double)5);//double 형변환
		System.out.println((double)13/(double)5); //double 형변환
		System.out.println(13.0/5); //double or float 형변환
		System.out.println(13/5.0); //double or float 형변환
		System.out.println((double)13/5); //double 형변환
	}
}
